// ---------- State ----------
const yearlyState = {
  currentYear: 2026
};

// ---------- Render function ----------
function renderYearlyView() {
  const container = document.getElementById('view-yearly');
  if (!container) return;

  const year = yearlyState.currentYear;
  const data = YEARLY_EVENTS[year];

  let html = `
    <div class="panel">
      <div class="year-controls">
        <button class="ghost" onclick="changeYear(-1)">‹ ${year - 1}</button>
        <h2 style="margin:0;">${year} ஆண்டு நிகழ்வுகள்</h2>
        <button class="ghost" onclick="changeYear(1)">${year + 1} ›</button>
      </div>
  `;

  if (!data) {
    html += `<p style="text-align:center; color:var(--silver);">${year} ஆண்டுக்கான தரவு இன்னும் சேர்க்கப்படவில்லை.</p>`;
  } else {
    html += `<div class="year-groups">`;
    for (const [groupName, items] of Object.entries(data)) {
      html += `
        <div class="year-group">
          <h3>${groupName} (${items.length})</h3>
      `;
      for (const [start, end] of items) {
        html += `
          <div class="year-item">
            <span class="yi-date">${start}</span>
            <span class="yi-time">→ ${end}</span>
          </div>
        `;
      }
      html += `</div>`;
    }
    html += `</div>`;
  }

  html += `
      <p class="disclaimer">* கணக்கீடு தோராயமானவை (Lahiri Ayanamsa அடிப்படையில்).</p>
    </div>
  `;

  container.innerHTML = html;
}

// ---------- Year navigation ----------
function changeYear(delta) {
  yearlyState.currentYear += delta;
  renderYearlyView();
}

// ---------- Hook into existing switchView (if defined in ui.js) ----------
// switchView('yearly') அழைக்கும் போது இது தானாக render ஆகும்.
document.addEventListener('DOMContentLoaded', () => {
  const yearlyTabBtn = document.querySelector('.tab-btn[data-view="yearly"]');
  if (yearlyTabBtn) {
    yearlyTabBtn.addEventListener('click', renderYearlyView);
  }
});